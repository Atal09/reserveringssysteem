<section class="intro">
<h3>
    Meestal volgt men bij een behandeling een vastgestelde behandelmethode met een specifieke opbouw.
</h3>
<p>
    Op deze pagina hebben we de algemene structuur en vorm van een gemiddelde behandeling beschreven.
    Echter, aangezien elke behandeling specifiek is afgestemd op de persoonlijke klachten, capaciteiten en wensen van de individuele patiënt, kan de uiteindelijke behandeling afwijken van de weergegeven structuur.
</p>
</section>

<section>
    <div>
        <h2>
            1. Aanmelden
        </h2>
        <p>
            Als eerste stap dient men zich aan te melden bij Mentaal Beter.
            Men kan zich direct aanmelden, echter voor het begin van de behandeling is een verwijsbrief nodig.
            Nadat de aanmelding voltooid is, zullen we een intakegesprek plannen en toegang geven tot ons cliëntenportaal.
            In ons cliëntenportaal kunt u na de intake aan de slag gaan met online e-health modules.
            Hiermee heeft u zelf een grote invloed op de snelheid van uw vooruitgang.
        </p>
    </div>
</section>



<section>
    <div>
        <h2>
            2.	Telefonische screening
        </h2>

        <p>
            Om te bepalen of we een geschikte behandeling kunnen bieden, zal er een kort telefonisch gesprek worden gepland met een van onze behandelaren.
            Als blijkt uit deze screening dat we u verder kunnen helpen met uw zorgvraag, zullen we een intakegesprek plannen.
            Na een succesvolle telefonische screening krijgt u toegang tot de e-health.
            De kosten van de telefonische screening worden in rekening gebracht bij uw zorgverzekeraar, ook als er vervolgens geen behandeling plaatsvindt.
        </p>

    </div>

</section>

<section>
    <div>
        <h2>
            3.	Intake en diagnose
        </h2>

        <p>
            Tijdens één of meerdere gesprekken willen we een duidelijk beeld vormen van uw klachten en levensgeschiedenis.
            Na de intake zullen we een behandelplan opstellen waarin de diagnose, behandelvorm, werkwijze, doelen en verwachte duur van de behandeling worden beschreven.
            Soms is aanvullend psychologisch onderzoek nodig om tot een zorgvuldige diagnose en behandeladvies te komen.
            In dat geval zal de intake en diagnose fase langer duren. Een goede diagnose is belangrijk om de juiste aanpak te bepalen.
            Het kan ook zijn dat wij niet de benodigde zorg kunnen leveren en u moeten doorverwijzen.
            Tijdens de behandeling is het mogelijk dat familie en naasten worden betrokken.
            Lees in ons Kwaliteitsstatuut hoe we dit vormgeven.
        </p>
    </div>
</section>

<section>
    <div>
        <h2>
            4.	Adviesgesprek
        </h2>
        <p>
            Tijdens het adviesgesprek bespreken we de opzet van het behandelplan en beslissen we samen of de aanpak aansluit bij uw behoeften.
        </p>

    </div>
</section>

<section>
    <div>
        <h2>
            5.	Behandelafspraken
        </h2>
        <p>
            Zodra het behandelplan is besproken, zullen de behandelafspraken worden ingepland.
            Het aantal afspraken hangt af van de ernst van de klachten en de snelheid van uw vooruitgang.
        </p>
    </div>
